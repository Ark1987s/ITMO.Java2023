package Lab01;

public class Ex02 {
    public static void main(String[] args) {
        short i;
        i = (46 + 10) * (10 / 3);
        System.out.println(i);

        float a;
        a = (46 + 10) * (10f / 3);
        System.out.println(a);

        System.out.println((46 + 10) * ((float) 10 / 3));

        int b;
        b = (29) * (4) * (-15);
        System.out.println(b);

        System.out.println((29) * (4) * (-15));
    }
}
